package FORLOOP;

public class ForReverseNumber {
    public static void main(String[] args) {
        //it is important,
        for(int i=0;i<10;i++){
            System.out.print(i);
        }

        System.out.println();

        for(int i=10;i>=1;i--){
            System.out.print(i);
        }
    }
}

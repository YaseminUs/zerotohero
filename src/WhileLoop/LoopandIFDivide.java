package WhileLoop;

public class LoopandIFDivide {
    public static void main(String[] args) {

       // print the number if number can divided with 15
        int num1 = 10;
        int num2 = 100;
        while (num1 <= num2) {

            if (num1 % 15== 0) {
                System.out.println(num1);
            }
            num1++;
        }





    }
}

package Switch;

public class SwitchCharPractice {
    public static void main(String[] args) {
        char ch='a';
        final char hr='h';
        switch (ch){

            case 'a':
                System.out.println("the value is apple");
                break;
            case 'b':
                System.out.println("the value is blackberry");
                break;
            case hr:
                System.out.println("the value is HR");
                break;

        }
    }
}
